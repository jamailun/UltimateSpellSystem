package fr.jamailun.ultimatespellsystem.api.bind;

/**
 * Type of argument for a spell cost.
 */
public enum SpellCostArgType {

  /**
   * Integer type.
   */
  INTEGER,

  /**
   * Floating point type.
   */
  DOUBLE,

  /**
   * Boolean type.
   */
  BOOLEAN,

  /**
   * Raw string.
   */
  STRING
}
